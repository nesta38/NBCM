def get_translation(key, lang='en'):
    """Récupère la traduction pour une clé donnée."""
    if key in TRANSLATIONS:
        return TRANSLATIONS[key].get(lang, TRANSLATIONS[key].get('en', key))
    return key


def get_user_language():
    """Récupère la langue de l'utilisateur courant."""
    from flask_login import current_user
    if current_user and current_user.is_authenticated:
        return current_user.language or 'en'
    return 'en'


def t(key):
    """Fonction raccourcie pour les templates."""
    return get_translation(key, get_user_language())
